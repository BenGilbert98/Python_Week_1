# sets and frozen sets -data collection with sets
# indexing - there is no indexing in sets
# sets are unordered
# Syntax: {} for sets

car_parts = {"Wheels", "Doors", "Radio"}
print(car_parts)

# managing data with sets

car_parts.add("Seatbelts") # adds "Seatbelts"
print(car_parts)

car_parts.remove("Wheels") # removes "Wheels"
print(car_parts)